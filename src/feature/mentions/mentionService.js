const { Mention , Tweet , User } = require('../../utils/index');


class MentionService {
            async createMention(tweetId , mentionedUserId) {

                const tweet = await Tweet.findByPk(tweetId);
                if(!tweet){
                    throw new Error('Tweet not found');
                }

                const user = await User.findByPk(mentionedUserId);
                if(!user){
                    throw new Error('User not found');
                }

                const mention = await Mention.create({
                    tweetId, mentionedUserId
                });
                return mention;
            }
}


module.exports = MentionService;
































































































































































































































































































































































































































































